# TypeCasting : Change one data type to another data type.

# Two types of typecasting :
# 1. Implicit typecasting : Done via python compiler
# 2. Explicit typecasting : Done via programmer or user

a = int(3)
b = str(4)
c = float(5)

print(a)
print(b)
print(c)

print(type(a))
print(type(b))
print(type(c))